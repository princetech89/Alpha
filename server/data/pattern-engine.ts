// 28. DOJI VARIANTS  (Candlestick · Swing)
// ══════════════════════════════════════════════════════════════════════════════
function detectDoji(candles: Candle[], indicators: Indicators): Signal | null {
  if (candles.length < 20) return null;
  const c    = candles[candles.length - 1];
  const body = Math.abs(c.close - c.open);
  const totalRange = c.high - c.low;
  if (totalRange === 0) return null;

  // Doji: body is < 10% of total range
  const bodyRatio = body / totalRange;
  if (bodyRatio > 0.10) return null;

  const upperWick = c.high - Math.max(c.open, c.close);
  const lowerWick = Math.min(c.open, c.close) - c.low;

  // Classify: Dragonfly (no upper wick, long lower), Gravestone (no lower wick, long upper), standard
  const isDragonfly  = upperWick < totalRange * 0.1 && lowerWick > totalRange * 0.6;
  const isGravestone = lowerWick < totalRange * 0.1 && upperWick > totalRange * 0.6;

  // Context: after a trend
  const priorCandles = candles.slice(-11, -1);
  const priorChange = priorCandles[priorCandles.length - 1].close - priorCandles[0].close;
  const trendWasUp   = priorChange > 0;
  const trendWasDown = priorChange < 0;

  // Dragonfly after downtrend = bullish; Gravestone after uptrend = bearish
  const isBullish = isDragonfly || (!isGravestone && trendWasDown);
  const patternName = isDragonfly ? "Dragonfly Doji" : isGravestone ? "Gravestone Doji" : "Doji";

  // Only fire on meaningful trend context
  if (patternName === "Doji" && Math.abs(priorChange) / priorCandles[0].close < 0.02) return null;

  const ltp = c.close;
  const atrVal = indicators.atr;
  const stopLossPrice = isBullish ? (c.low * 0.99) : (c.high * 1.01);
  const target1Price  = isBullish ? (ltp + atrVal * 1.5) : (ltp - atrVal * 1.5);
  const target2Price  = isBullish ? (ltp + atrVal * 2.5) : (ltp - atrVal * 2.5);
  const riskAmt       = Math.abs(ltp - stopLossPrice);
  const rewardAmt     = Math.abs(target2Price - ltp);
  const rrRatio       = riskAmt > 0 ? (rewardAmt / riskAmt).toFixed(1) : "—";

  const { score, breakdown } = calculateArithmeticScore(62, candles, isBullish, indicators, 0);
  const volConfirmed = c.volume > indicators.vol20Avg * 1.0;

  const enExpl = isDragonfly
    ? `A Dragonfly Doji has formed after a ${trendWasDown ? "downtrend" : "sideways phase"}. The candle opened and closed near ₹${ltp.toFixed(2)} but dipped to ₹${c.low.toFixed(2)} intraday — sellers pushed prices lower but buyers absorbed every unit, closing at the open. This is a powerful single-candle reversal signal at potential support, especially after a sustained decline.`
    : isGravestone
    ? `A Gravestone Doji has formed after an ${trendWasUp ? "uptrend" : "sideways phase"}. The candle opened and closed near ₹${ltp.toFixed(2)} but reached ₹${c.high.toFixed(2)} intraday — buyers initially pushed prices higher but sellers overwhelmed them, closing at the open. This is a single-candle bearish reversal signal at potential resistance.`
    : `A Doji has formed (open ≈ close at ₹${ltp.toFixed(2)}, range: ₹${c.low.toFixed(2)}–₹${c.high.toFixed(2)}). This indecision candle after a ${trendWasUp ? "rally" : "decline"} signals that the market has reached a balance point — the prior trend may be exhausting. Confirmation from the next session's direction is critical.`;

  const hiExpl = isDragonfly
    ? `ड्रैगनफ्लाई डोजी बना है। लंबी निचली छाया (₹${c.low.toFixed(2)}) और ओपन-क्लोज बराबर (₹${ltp.toFixed(2)}) — बिकवालों ने कीमत नीचे धकेली लेकिन खरीदारों ने वापस ला दी। तेजी के उलटाव का मजबूत संकेत।`
    : isGravestone
    ? `ग्रेवस्टोन डोजी बना है। लंबी ऊपरी छाया (₹${c.high.toFixed(2)}) और ओपन-क्लोज बराबर (₹${ltp.toFixed(2)}) — खरीदार कमजोर पड़ गए। मंदी के उलटाव का संकेत।`
    : `डोजी बना है — बाजार में अनिश्चितता। ओपन ≈ क्लोज (₹${ltp.toFixed(2)}) लेकिन रेंज ₹${c.low.toFixed(2)}–₹${c.high.toFixed(2)}। अगले सत्र की दिशा पुष्टि करेगी।`;

  return {
    id: `doji_${Date.now()}`,
    patternName,
    type: isBullish ? "Bullish" : "Bearish",
    patternCategory: "Candlestick",
    timeframeClass: "Swing",
    explanation: { en: enExpl, hi: hiExpl },
    confidenceScore: score,
    confidenceBreakdown: breakdown,
    timeframe: "1D",
    successRate: isDragonfly ? 61.4 : isGravestone ? 59.8 : 55.3,
    historicalOccurrences: 0,
    entry: isBullish ? `${(ltp * 1.002).toFixed(2)} – ${(ltp * 1.007).toFixed(2)}` : `Below ${ltp.toFixed(2)}`,
    stopLoss: stopLossPrice.toFixed(2),
    target1: target1Price.toFixed(2),
    target2: target2Price.toFixed(2),
    riskReward: `1:${rrRatio}`,
    volumeConfirmed: volConfirmed,
    disclaimer: DISCLAIMER,
    detectedAt: new Date().toISOString(),
  };
}


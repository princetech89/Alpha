// 26. RISING WEDGE  (Reversal · Swing)
// ══════════════════════════════════════════════════════════════════════════════
function detectRisingWedge(candles: Candle[], indicators: Indicators): Signal | null {
  if (candles.length < 30) return null;
  const slice  = candles.slice(-30);
  const highs  = slice.map(c => c.high);
  const lows   = slice.map(c => c.low);

  // Both trendlines rising, but lows rising faster than highs (converging)
  const highsSlope  = (Math.max(...highs.slice(-8)) - Math.max(...highs.slice(0, 8))) / Math.max(...highs.slice(0, 8));
  const lowsSlope   = (Math.min(...lows.slice(-8))  - Math.min(...lows.slice(0, 8)))  / Math.min(...lows.slice(0, 8));

  // Both rising
  if (highsSlope <= 0 || lowsSlope <= 0) return null;
  // Lows rising faster than highs (wedge converging)
  if (lowsSlope <= highsSlope * 1.2) return null;

  // Pattern should be at least 3 weeks old but not more than 6 weeks
  const patternRange = Math.max(...highs) - Math.min(...lows);
  const currentRange = Math.max(...highs.slice(-8)) - Math.min(...lows.slice(-8));
  if (currentRange >= patternRange * 0.7) return null; // range must be tightening

  const ltp = candles[candles.length - 1].close;
  const wedgeLow = Math.min(...lows.slice(-5));
  const wedgeHigh = Math.max(...highs);

  const stopLossPrice = Math.max(...highs.slice(-5)) * 1.015;
  const target1Price  = ltp * 0.95;
  const target2Price  = ltp - (wedgeHigh - Math.min(...lows));
  const riskAmt       = stopLossPrice - ltp;
  const rewardAmt     = ltp - target2Price;
  const rrRatio       = riskAmt > 0 ? (rewardAmt / riskAmt).toFixed(1) : "—";

  const { score, breakdown } = calculateArithmeticScore(66, candles, false, indicators, 0);
  const volConfirmed = candles[candles.length - 1].volume < indicators.vol20Avg * 0.9; // declining volume in wedge

  return {
    id: `rw_${Date.now()}`,
    patternName: "Rising Wedge",
    type: "Bearish",
    patternCategory: "Reversal",
    timeframeClass: "Swing",
    explanation: {
      en: `A Rising Wedge pattern is forming — a bearish reversal signal. Both the highs (rising ${(highsSlope * 100).toFixed(1)}%) and lows (rising ${(lowsSlope * 100).toFixed(1)}%) are trending upward, but the lows are rising faster, causing the trading range to compress into a wedge shape (current range: ₹${currentRange.toFixed(2)} vs earlier: ₹${patternRange.toFixed(2)}). This compression on declining volume indicates weakening buying pressure despite higher prices. Rising wedges typically resolve to the downside.`,
      hi: `राइजिंग वेज पैटर्न बन रहा है — एक मंदी उलटाव संकेत। ऊँचे और निचले स्तर दोनों बढ़ रहे हैं, लेकिन रेंज सिकुड़ रही है। यह कमजोर खरीदारी का संकेत है और आमतौर पर नीचे की ओर ब्रेकडाउन होता है।`,
    },
    confidenceScore: score,
    confidenceBreakdown: breakdown,
    timeframe: "1D",
    successRate: 69.3,
    historicalOccurrences: 0,
    entry: `Below ${wedgeLow.toFixed(2)}`,
    stopLoss: stopLossPrice.toFixed(2),
    target1: target1Price.toFixed(2),
    target2: target2Price.toFixed(2),
    riskReward: `1:${rrRatio}`,
    volumeConfirmed: volConfirmed,
    disclaimer: DISCLAIMER,
    detectedAt: new Date().toISOString(),
  };
}


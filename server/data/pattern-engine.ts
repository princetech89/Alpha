// MAIN ENTRY POINT
// ══════════════════════════════════════════════════════════════════════════════
export async function detectPatterns(candles: Candle[]): Promise<{ signals: Signal[], indicators: Indicators }> {
  const indicators = await computeIndicators(candles);

  const detectors: Array<(c: Candle[], i: Indicators) => Signal | null> = [
    detectMACross,
    detectRSIDivergence,
    detectBearishRSIDivergence,
    detectDoubleBottom,
    detectDoubleTop,
    detectHeadAndShoulders,
    detectInvertedHeadAndShoulders,
    detectCupAndHandle,
    detectAscendingTriangle,
    detectDescendingTriangle,
    detectHighBreakout,
    detectHammer,
    detectShootingStar,
    detectBullishEngulfing,
    detectBearishEngulfing,
    detectThreeWhiteSoldiers,
    detectThreeBlackCrows,
    detectMorningStar,
    detectEveningStar,
    detectMACDCrossover,
    detectBollingerSqueeze,
    detectSupportBounce,
    detectResistanceRejection,
    detectBullFlag,
    detectBearFlag,
    detectRisingWedge,
    detectFallingWedge,
    detectDoji,
  ];

  // Hold period per timeframe class for backtesting
  const holdPeriods: Record<string, number> = {
    Intraday:   3,
    Swing:      10,
    Positional: 20,
  };

  const signals: Signal[] = [];
  for (const detectFunc of detectors) {
    try {
      const sig = detectFunc(candles, indicators);
      if (sig) {
        // Per-stock backtest: override static successRate with real walk-forward rate
        if (candles.length >= 120) {
          const hp = holdPeriods[sig.timeframeClass] ?? 10;
          const bt = backtestPattern(candles, detectFunc, hp);
          if (bt.occurrences >= 1) {
            sig.successRate = bt.successRate;
            sig.historicalOccurrences = bt.occurrences;
          }
        }
        signals.push(sig);
      }
    } catch (err: any) {
      console.warn(`[PatternEngine] Detector error: ${err.message}`);
    }
  }

  return {
    signals: signals.sort((a, b) => b.confidenceScore - a.confidenceScore),
    indicators
  };
}

// ── Parse Angel One candle response ──────────────────────────────────────────
// Angel One returns: [timestamp, open, high, low, close, volume]
export function parseAngelCandles(raw: any[][]): Candle[] {
  if (!Array.isArray(raw)) return [];
  return raw
    .filter(row => Array.isArray(row) && row.length >= 6)
    .map(row => ({
      date:   new Date(row[0]).toLocaleDateString("en-CA", { timeZone: "Asia/Kolkata" }),
      open:   Number(row[1]),
      high:   Number(row[2]),
      low:    Number(row[3]),
      close:  Number(row[4]),
      volume: Number(row[5]),
    }))
    .filter(c => c.close > 0 && c.high > 0);
}

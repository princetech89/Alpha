// ── Math Helpers ──────────────────────────────────────────────────────────────
function sma(candles: Candle[], period: number): number[] {
  const result: number[] = [];
  for (let i = 0; i < candles.length; i++) {
    if (i < period - 1) { result.push(NaN); continue; }
    const slice = candles.slice(i - period + 1, i + 1);
    result.push(slice.reduce((s, c) => s + c.close, 0) / period);
  }
  return result;
}

function avgVolume(candles: Candle[], period = 20): number {
  const slice = candles.slice(-period - 1, -1); // exclude last candle
  return slice.reduce((s, c) => s + c.volume, 0) / slice.length;
}

function rsiSeries(candles: Candle[], period = 14): number[] {
  const closes = candles.map(c => c.close);
  const result: number[] = new Array(period).fill(NaN);
  let gains = 0, losses = 0;
  for (let i = 1; i <= period; i++) {
    const diff = closes[i] - closes[i - 1];
    if (diff >= 0) gains += diff; else losses -= diff;
  }
  let avgGain = gains / period;
  let avgLoss = losses / period;
  result.push(100 - 100 / (1 + avgGain / (avgLoss || 0.0001)));
  for (let i = period + 1; i < closes.length; i++) {
    const diff = closes[i] - closes[i - 1];
    avgGain = (avgGain * (period - 1) + Math.max(diff, 0)) / period;
    avgLoss = (avgLoss * (period - 1) + Math.max(-diff, 0)) / period;
    result.push(100 - 100 / (1 + avgGain / (avgLoss || 0.0001)));
  }
  return result;
}

function atr(candles: Candle[], period = 14): number {
  const trs: number[] = [];
  for (let i = 1; i < candles.length; i++) {
    const hl = candles[i].high - candles[i].low;
    const hc = Math.abs(candles[i].high - candles[i - 1].close);
    const lc = Math.abs(candles[i].low - candles[i - 1].close);
    trs.push(Math.max(hl, hc, lc));
  }
  const recent = trs.slice(-period);
  return recent.reduce((a, b) => a + b, 0) / recent.length;
}

/** 
 * Composite arithmetic confidence score: 
 * + 25 if all pattern pivots are clearly present in data
 * + 20 if volume confirms (breakout volume > 1.5× 20D avg)
 * + 15 if RSI supports (Bullish: <45 or >60, Bearish: >55)
 * + 15 if 50D MA direction matches pattern
 * + 15 if MACD histogram matches pattern
 * + 10 if backtest occurrence count > 10
 */
function calculateArithmeticScore(
  baseScore: number, 
  candles: Candle[], 
  isBullish: boolean, 
  indicators: Indicators,
  backtestCount: number
): { score: number; breakdown: string } {
  let score = 25; // Base pivot score
  let breakdown = ["- Pattern pivots present (+25)"];

  const n = candles.length - 1;
  const volRatio = candles[n].volume / indicators.vol20Avg;
  if (volRatio > 1.5) {
    score += 20;
    breakdown.push(`- High volume confirmation ${volRatio.toFixed(1)}x (+20)`);
  } else if (volRatio > 1.1) {
    score += 10;
    breakdown.push("- Moderate volume buildup (+10)");
  }

  const r = indicators.rsi;
  if (isBullish) {
    if (r < 40) { score += 15; breakdown.push("- Oversold RSI strength (+15)"); }
    else if (r > 60) { score += 15; breakdown.push("- Bullish momentum RSI (+15)"); }
  } else {
    if (r > 60) { score += 15; breakdown.push("- Overbought RSI weakness (+15)"); }
  }

  // MA Trend Alignment (simple EMA20 > EMA50 check)
  if (isBullish && indicators.sma20 > indicators.sma50) {
    score += 15;
    breakdown.push("- SMA Trend alignment (+15)");
  } else if (!isBullish && indicators.sma20 < indicators.sma50) {
    score += 15;
    breakdown.push("- SMA Trend alignment (+15)");
  }

  if (isBullish && indicators.macd.histogram > 0) {
    score += 15;
    breakdown.push("- MACD Histogram bullish (+15)");
  } else if (!isBullish && indicators.macd.histogram < 0) {
    score += 15;
    breakdown.push("- MACD Histogram bearish (+15)");
  }

  if (backtestCount > 10) {
    score += 10;
    breakdown.push("- High historical statistical reliability (+10)");
  }

  return { 
    score: Math.min(100, score), 
    breakdown: breakdown.join("\n") 
  };
}

function calculateIndicators(candles: Candle[]): Indicators {
  const n = candles.length - 1;
  const sma20Series = sma(candles, 20);
  const sma50Series = sma(candles, 50);
  const rsi = rsiSeries(candles);
  const atrVal = atr(candles);
  const volAvg = avgVolume(candles, 20);

  // MACD (12, 26, 9)
  const ema12 = calculateEMA(candles.map(c => c.close), 12);
  const ema26 = calculateEMA(candles.map(c => c.close), 26);
  const macdVal = ema12[n] - ema26[n];
  const signalSeries = calculateEMA(ema12.map((v, i) => v - ema26[i]), 9);
  const signalVal = signalSeries[n];

  // Bollinger Bands
  const std = calculateStdDev(candles.map(c => c.close).slice(-20), sma20Series[n] || 0);
  const upper = (sma20Series[n] || 0) + (std * 2);
  const lower = (sma20Series[n] || 0) - (std * 2);

  return {
    rsi: rsi[n] || 50,
    rsiDivergence: false, // will be updated if detectRSIDivergence is used
    sma20: sma20Series[n] || 0,
    sma50: sma50Series[n] || 0,
    macd: { macd: macdVal, signal: signalVal, histogram: macdVal - signalVal },
    bollinger: { upper, middle: sma20Series[n] || 0, lower },
    atr: atrVal,
    vol20Avg: volAvg,
  };
}

function calculateEMA(data: number[], period: number): number[] {
  const k = 2 / (period + 1);
  const results: number[] = [data[0]];
  for (let i = 1; i < data.length; i++) {
    results.push(data[i] * k + results[i - 1] * (1 - k));
  }
  return results;
}

function calculateStdDev(data: number[], mean: number): number {
  if (data.length === 0) return 0;
  const variance = data.reduce((acc, val) => acc + Math.pow(val - mean, 2), 0) / data.length;
  return Math.sqrt(variance);
}

/** Average Directional Index — measures trend strength (not direction) */
function adx(candles: Candle[], period = 14): number {
  if (candles.length < period + 2) return 25; // default neutral
  const trs: number[] = [];
  const plusDMs: number[] = [];
  const minusDMs: number[] = [];
  for (let i = 1; i < candles.length; i++) {
    const hl  = candles[i].high - candles[i].low;
    const hpc = Math.abs(candles[i].high - candles[i - 1].close);
    const lpc = Math.abs(candles[i].low  - candles[i - 1].close);
    trs.push(Math.max(hl, hpc, lpc));
    const upMove   = candles[i].high - candles[i - 1].high;
    const downMove = candles[i - 1].low - candles[i].low;
    plusDMs.push(upMove > downMove && upMove > 0 ? upMove : 0);
    minusDMs.push(downMove > upMove && downMove > 0 ? downMove : 0);
  }
  const smoothedTR   = trs.slice(-period).reduce((a, b) => a + b, 0);
  const smoothedPlus = plusDMs.slice(-period).reduce((a, b) => a + b, 0);
  const smoothedMinus = minusDMs.slice(-period).reduce((a, b) => a + b, 0);
  if (smoothedTR === 0) return 25;
  const diPlus  = (smoothedPlus  / smoothedTR) * 100;
  const diMinus = (smoothedMinus / smoothedTR) * 100;
  const diSum   = diPlus + diMinus;
  if (diSum === 0) return 25;
  return Math.abs(diPlus - diMinus) / diSum * 100;
}


// PER-STOCK BACKTESTING — Walk-forward success rate calculation
// ══════════════════════════════════════════════════════════════════════════════
/**
 * Runs a detector on historical slices of candle data to compute the real
 * success rate for that specific stock + pattern combination.
 * Walk-forward: starts at index 65 (enough for all indicators) and moves forward,
 * checking if price moved ≥3% in the predicted direction within holdPeriod days.
 */

// ── Backtest result cache ─────────────────────────────────────────────────────
const btCache = new Map<string, { result: { successRate: number; occurrences: number }; expires: number }>();

export function backtestPattern(
  candles: Candle[],
  detectorFn: (c: Candle[], i: Indicators) => Signal | null,
  holdPeriod = 10,
  minMove = 0.03
): { successRate: number; occurrences: number } {
  // Cache key: use first+last candle dates + function name + holdPeriod
  const cacheKey = `${candles[0]?.date}_${candles[candles.length - 1]?.date}_${detectorFn.name}_${holdPeriod}`;
  const cached = btCache.get(cacheKey);
  if (cached && cached.expires > Date.now()) return cached.result;

  const startIdx = 65;
  if (candles.length < startIdx + holdPeriod + 5) {
    return { successRate: 0, occurrences: 0 };
  }

  let wins = 0;
  let total = 0;

  for (let i = startIdx; i < candles.length - holdPeriod - 1; i++) {
    const slice = candles.slice(0, i + 1);
    const indicators = calculateIndicators(slice);
    let sig: Signal | null = null;
    try {
      sig = detectorFn(slice, indicators);
    } catch {
      continue;
    }
    if (!sig) continue;

    total++;
    const entryPrice = candles[i].close;
    const isBullish = sig.type === "Bullish";

    let hit = false;
    for (let j = i + 1; j <= i + holdPeriod && j < candles.length; j++) {
      const move = (candles[j].close - entryPrice) / entryPrice;
      if (isBullish && move >= minMove) { hit = true; break; }
      if (!isBullish && move <= -minMove) { hit = true; break; }
    }
    if (hit) wins++;
  }

  const result = total === 0
    ? { successRate: 0, occurrences: 0 }
    : { successRate: Math.round((wins / total) * 100 * 10) / 10, occurrences: total };

  // Cache for 24 hours
  btCache.set(cacheKey, { result, expires: Date.now() + 24 * 60 * 60 * 1000 });
  return result;
}

